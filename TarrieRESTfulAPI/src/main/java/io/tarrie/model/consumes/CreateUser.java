package io.tarrie.model.consumes;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;
import io.tarrie.Utility;
import io.tarrie.database.contants.DbAttributes;
import io.tarrie.database.contants.DbConstants;
import io.tarrie.model.constants.CharacterLimit;

import javax.mail.internet.AddressException;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import java.util.Date;

@DynamoDBTable(tableName = DbConstants.BASE_TABLE)
@ApiModel
public class CreateUser {

    private String imgPath;
    private String idCopy;
    private String id;
    private String name;
    private String emailAddr;
    private Date createdDate;


    @DynamoDBHashKey(attributeName =DbAttributes.HASH_KEY)
    @ApiModelProperty(notes = "The unique identifier for the entity", example = "xr563",  position = 0)
    @Size(min=1, max= CharacterLimit.SMALL)
    @NotNull
    public String getId() {
        return id;
    }


    @DynamoDBAttribute(attributeName =DbAttributes.NAME)
    @ApiModelProperty(notes = "The name of the entity", example = "Becky")
    @NotNull
    @Size(min=1, max= CharacterLimit.SMALL)
    public String getName() {
        return name;
    }

    @DynamoDBAttribute(attributeName = DbAttributes.DATA)
    @ApiModelProperty(notes = "The email address of the user")
    @NotNull
    public String getEmailAddr() {
        return emailAddr;
    }

    @DynamoDBRangeKey(attributeName =DbAttributes.SORT_KEY)
    public String getIdCopy() {
        return idCopy;
    }

    @DynamoDBAttribute(attributeName =DbAttributes.IMG_PATH)
    @DynamoDBAutoGeneratedDefault(DbConstants.DEFAULT_USER_IMG)
    public String getImgPath() {
        return imgPath;
    }

    @DynamoDBAttribute(attributeName = DbAttributes.CREATED_TIME)
    @ApiModelProperty(notes="Date the user was created")
    @DynamoDBAutoGeneratedTimestamp
    public Date getCreatedDate() {
        return createdDate;
    }

    @ApiModelProperty(notes = "The unique identifier for the entity", example = "xr563",  position = 0)
    @Size(min=1, max= CharacterLimit.SMALL)
    @NotNull
    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setIdCopy(String idCopy) {
        this.idCopy = idCopy;
    }

    public void setEmailAddr(String emailAddr) throws AddressException {
        this.emailAddr= Utility.getEmailAddressFromString(emailAddr).toString();
    }

    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    public void setImgPath(String imgPath) {
        this.imgPath = imgPath;
    }
}
